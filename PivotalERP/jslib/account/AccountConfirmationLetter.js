    $scope.GetAccountConfirmationLetter = function () {

        var dateFrom = new Date(($filter('date')(new Date(), 'yyyy-MM-dd')));
        var dateTo = new Date(($filter('date')(new Date(), 'yyyy-MM-dd')));

        if ($scope.AccountConfirmationLetter.DateFromDet)
            dateFrom = new Date(($filter('date')($scope.AccountConfirmationLetter.DateFromDet.dateAD, 'yyyy-MM-dd')));

        if ($scope.AccountConfirmationLetter.DateToDet)
            dateTo = new Date(($filter('date')($scope.AccountConfirmationLetter.DateToDet.dateAD, 'yyyy-MM-dd')));

        $scope.DataColl = []; //declare an empty array
        $scope.gridOptions.api.setRowData($scope.DataColl);

        var beData = {
            DateFrom: dateFrom,
            DateTo: dateTo,
            isPurchase: $scope.AccountConfirmationLetter.isPurchase,
        };

        $scope.loadingstatus = 'running';

        $http({
            method: "post",
            url: base_url + "Account/Reporting/GetAccountConfirmationLetter",
            data: JSON.stringify(beData),
            dataType: "json"
        }).then(function (res) {

            $scope.DataColl = res.data.Data;
            $scope.gridOptions.api.setRowData($scope.DataColl);

            $scope.loadingstatus = 'done';

        }, function (errormessage) {

            $scope.loadingstatus = 'stop';

            alert('Unable to Store data. pls try again.' + errormessage.responseText);
        });

    };

